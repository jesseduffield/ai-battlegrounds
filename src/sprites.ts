// Cache for rendered sprites
const spriteCache: Map<string, HTMLCanvasElement> = new Map();

// Render a sprite grid to a canvas
export function renderSpriteToCanvas(sprite: SpriteGrid): HTMLCanvasElement {
  const canvas = document.createElement("canvas");
  canvas.width = 16;
  canvas.height = 16;
  const ctx = canvas.getContext("2d")!;
  const imageData = ctx.createImageData(16, 16);
  const data = imageData.data;

  for (let y = 0; y < 16; y++) {
    for (let x = 0; x < 16; x++) {
      const color = sprite[y][x];
      const idx = (y * 16 + x) * 4;
      if (color === null) {
        data[idx] = 0;
        data[idx + 1] = 0;
        data[idx + 2] = 0;
        data[idx + 3] = 0; // Transparent
      } else {
        data[idx] = (color >> 16) & 0xff; // R
        data[idx + 1] = (color >> 8) & 0xff; // G
        data[idx + 2] = color & 0xff; // B
        data[idx + 3] = 255; // A
      }
    }
  }

  ctx.putImageData(imageData, 0, 0);
  return canvas;
}

// Render a character sprite with custom colors
export function renderCharacterSprite(
  template: SpriteGrid,
  bodyColor: number,
  accentColor: number
): HTMLCanvasElement {
  // Calculate darker shade of body color
  const darken = (color: number, amount: number): number => {
    const r = Math.max(0, ((color >> 16) & 0xff) - amount);
    const g = Math.max(0, ((color >> 8) & 0xff) - amount);
    const b = Math.max(0, (color & 0xff) - amount);
    return (r << 16) | (g << 8) | b;
  };

  const bodyDark = darken(bodyColor, 40);

  // Create a copy with replaced colors
  const colored: SpriteGrid = template.map((row) =>
    row.map((pixel) => {
      if (pixel === C.bodyMain) return bodyColor;
      if (pixel === C.bodyAccent) return accentColor;
      if (pixel === C.bodyDark) return bodyDark;
      return pixel;
    })
  );

  return renderSpriteToCanvas(colored);
}

// Get or create a cached sprite
export function getSprite(name: string): HTMLCanvasElement {
  if (spriteCache.has(name)) {
    return spriteCache.get(name)!;
  }

  let canvas: HTMLCanvasElement;

  switch (name) {
    case "ground":
      canvas = renderSpriteToCanvas(SPRITE_GROUND);
      break;
    case "wall":
      canvas = renderSpriteToCanvas(SPRITE_WALL);
      break;
    case "water":
      canvas = renderSpriteToCanvas(SPRITE_WATER);
      break;
    case "grass":
      canvas = renderSpriteToCanvas(SPRITE_GRASS);
      break;
    case "bars":
      canvas = renderSpriteToCanvas(SPRITE_BARS);
      break;
    case "door_closed":
      canvas = renderSpriteToCanvas(SPRITE_DOOR_CLOSED);
      break;
    case "door_locked":
      canvas = renderSpriteToCanvas(SPRITE_DOOR_LOCKED);
      break;
    case "door_open":
      canvas = renderSpriteToCanvas(SPRITE_DOOR_OPEN);
      break;
    case "chest":
      canvas = renderSpriteToCanvas(SPRITE_CHEST);
      break;
    case "chest_searched":
      canvas = renderSpriteToCanvas(SPRITE_CHEST_SEARCHED);
      break;
    case "trap":
      canvas = renderSpriteToCanvas(SPRITE_TRAP);
      break;
    case "character_dead":
      canvas = renderSpriteToCanvas(SPRITE_CHARACTER_DEAD);
      break;
    case "item_weapon":
      canvas = renderSpriteToCanvas(SPRITE_ITEM_WEAPON);
      break;
    case "item_consumable":
      canvas = renderSpriteToCanvas(SPRITE_ITEM_CONSUMABLE);
      break;
    case "item_key":
      canvas = renderSpriteToCanvas(SPRITE_ITEM_KEY);
      break;
    case "item_trap":
      canvas = renderSpriteToCanvas(SPRITE_ITEM_TRAP);
      break;
    case "item_clothing":
      canvas = renderSpriteToCanvas(SPRITE_ITEM_CLOTHING);
      break;
    case "item_contract":
      canvas = renderSpriteToCanvas(SPRITE_ITEM_CONTRACT);
      break;
    default:
      // Return empty canvas for unknown sprites
      canvas = document.createElement("canvas");
      canvas.width = 16;
      canvas.height = 16;
  }

  spriteCache.set(name, canvas);
  return canvas;
}

// Get a character sprite with specific colors
export function getCharacterSprite(
  bodyColor: number,
  accentColor: number,
  armed: boolean
): HTMLCanvasElement {
  const key = `char_${bodyColor.toString(16)}_${accentColor.toString(16)}_${
    armed ? "armed" : "unarmed"
  }`;

  if (spriteCache.has(key)) {
    return spriteCache.get(key)!;
  }

  const template = armed ? SPRITE_CHARACTER_ARMED : SPRITE_CHARACTER_UNARMED;
  const canvas = renderCharacterSprite(template, bodyColor, accentColor);
  spriteCache.set(key, canvas);
  return canvas;
}

// Parse hex color string to number
export function parseColor(color: string): number {
  if (color.startsWith("#")) {
    return parseInt(color.slice(1), 16);
  }
  return parseInt(color, 16);
}

